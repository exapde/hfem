            f_udg[3*ng+i] += D_ff_D_x*epsilon0*rampFactor*t40*u9;
            f_udg[4*ng+i] += epsilon0*ff*rampFactor*t47*(t35*u6+t2*t7*u2*2.0-t4*t18*u6*2.0-t4*t19*u6*2.0-t4*t20*u6*2.0+t2*t48*u3*2.0+t2*t49*u4*2.0)+D_ff_D_x*epsilon0*rampFactor*t40*t69;
